package sesac.spring.api.spring.rest.api.vo;

public class UserVO {
        private String name;
        private String age;
        private  String birth;
        private String hobby;
        private String gender;
        private String id;
        private String pw;

    public String getName() {
        return name;
    }

    public String getAge() {
        return age;
    }
    public String getHobby() {
        return hobby;
    }

    public String getGender() {
        return gender;
    }

    public String getBirth() {
        return birth;
    }

    public String getId() {
        return id;
    }

    public String getPw() {
        return pw;
    }
}
